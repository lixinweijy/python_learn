# -*- coding:utf-8 -*-
import turtle as t
t.pensize(10)
t.hideturtle()
t.color("red","#A42B2A")
t.penup()
t.goto(0,-150)
t.pendown()
t.setheading(0)
t.begin_fill()
t.circle(150,360)
t.end_fill()
t.penup()
t.setheading(90)
t.forward(100)
t.right(90)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.forward(50)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(50)
t.end_fill()
t.done()