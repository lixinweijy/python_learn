#coding=gbk
import turtle as t
x=90
t.setup(900,500)
t.bgcolor('black')
t.screensize(400,400)
t1=t.Pen()
t2=t.Pen()
t1.hideturtle()
t2.hideturtle()
def hhh():#画小方框
    t1.speed(0)
    t1.right(90)
    t1.pendown()
    t1.fd(2)
    t1.left(90)
    t1.fd(14)
    t1.left(90)
    t1.fd(4)
    t1.left(90)
    t1.fd(14)
    t1.left(90)
    t1.fd(2)
for i in range(100):
    t2.penup()
    t2.goto(-10,-12)
    t2.pendown()
    t2.color('pink')
    t2.clear()
    t2.write(i,font=('楷体',20))
    t1.begin_fill()
    t1.color('black','pink')
    t1.setheading(x)
    t1.penup()
    t1.fd(60)
    hhh()
    t1.end_fill()
    t1.penup()
    t1.goto(0,0)
    x-=3.6
t2.clear()
t2.penup()
t2.goto(-20,-12)
t2.pendown()
t2.write(100,font=('楷体',20))
