import turtle as t
t.setup(900,500)
t.screensize(400,400)
t1=t.Pen()
t2=t.Pen()
t3=t.Pen()
t1.hideturtle()
t2.hideturtle()
t3.hideturtle()
def hhh(x):
    t1.penup()
    t1.setheading(-90)
    t1.fd(x)
    t1.setheading(0)
    t1.pendown()
t1.speed(0)
hhh(50)
t1.pensize(3)
t1.pencolor('pink')
t1.circle(50)
hhh(10)
t1.circle(60)
t1.penup()
t1.goto(0,-55)
t1.pensize(6)
t1.pendown()
#画环形
for i in range(100):
        t2.penup()
        t2.goto(-20,-10)
        t2.pendown()
        t2.color('pink')
        t2.clear()
        t2.write(i,font=('楷体',20))
        t1.circle(55,3.6)
        t3.penup()
        t3.goto(20,-10)
        t3.pendown()
        t3.write('%',font=('楷体',20))
        t.delay(30)
        # t.tracer()
t2.clear()
t2.penup()
t2.goto(-25,-10)
t2.pendown()
t2.write(100,font=('楷体',20))
