# 计算0-4的累加和
'''
4步循环法
 1.初始化变量
 2.条件判断
 3.条件执行体
 4.改变变量
 总结：初始化的变量与条件判断的变量与改变的变量为同一个
 '''
sum=0 # 用于存储累加和
# 初始化变量为0
a=0
# 条件判断
while a<5:
    # 条件执行体
    sum+=a
    # 改变变量
    a+=1
print('和为',sum)

# 计算1-100之间都偶数和
sum=0 # 用于存储偶数和
a=1
while a<=100:
    if a%2==0: # a%2，bool值为False，可算奇数和
        sum+=a
    a+=1
print('1-100之间的偶数和',sum)

